#!/usr/bin/env python3
"""
bundle.py — build the published, anti-skid distributable for the hub.

Reads the readable modules in roblox/cinematic/, inlines them into ONE
self-contained Lua chunk (no per-module HttpGet), then wraps that chunk in a
junk-padded XOR+base64 decoder so the published file is not casually
copy-pasteable. The decoder is plain Luau (string ops + bit32), so any executor
that can run the current loadstring can run this.

This is light anti-skid, not real obfuscation: the readable source still lives
in cinematic/ for development, and a determined reader can recover the payload.
For stronger protection, run the emitted dist file through a dedicated Lua
obfuscator before publishing.

Usage:  python3 roblox/build/bundle.py
Output: roblox/dist/Mirage.lua
"""

import base64
import os
import random

HERE = os.path.dirname(os.path.abspath(__file__))
ROOT = os.path.dirname(HERE)                      # roblox/
SRC = os.path.join(ROOT, "cinematic")
OUT_DIR = os.path.join(ROOT, "dist")
OUT = os.path.join(OUT_DIR, "Mirage.lua")

# Module load order, mirroring CinematicHub.lua.
LIB = "Lib"
SHELL = "Shell"
TABS = ["FreeCam", "Shaders", "Fonts", "World", "Client", "Fun", "Extras"]

# XOR key for the payload. Cosmetic strength only.
KEY = "m1r4ge::xK9$do-not-skid::v1"


def read_module(name: str) -> str:
    with open(os.path.join(SRC, name + ".lua"), "r", encoding="utf-8") as f:
        return f.read()


def build_payload() -> str:
    parts = []
    parts.append("-- Mirage (bundled). Generated by build/bundle.py — edit cinematic/ instead.\n")
    parts.append("local Players = game:GetService('Players')\n")
    parts.append("if not game:IsLoaded() then game.Loaded:Wait() end\n")
    parts.append("if not Players.LocalPlayer then "
                 "Players:GetPropertyChangedSignal('LocalPlayer'):Wait() end\n")
    parts.append("local __M = {}\n")

    for name in [LIB, SHELL] + TABS:
        body = read_module(name)
        # Each module file is `... return <value>`; wrapping it in a function
        # and calling it reproduces what the HTTP loader did via chunk().
        parts.append("__M['%s'] = function()\n%s\nend\n" % (name, body))

    parts.append(
        "local __C = {}\n"
        "local function __R(n)\n"
        "  if __C[n] ~= nil then return __C[n] end\n"
        "  local v = __M[n]()\n"
        "  __C[n] = v\n"
        "  return v\n"
        "end\n"
    )
    parts.append("local Lib = __R('%s')\n" % LIB)
    parts.append("local ctx = __R('%s')(Lib)\n" % SHELL)
    for name in TABS:
        parts.append("__R('%s')(ctx, Lib)\n" % name)
    parts.append("ctx.selectTab('Free Cam')\n")
    parts.append(
        "print((\"[%s] Loaded. Tap '%s %s' or press ` to open. Press P for Free Cam.\")"
        ":format(Lib.BRAND, Lib.GLYPH, Lib.BRAND))\n"
    )
    return "".join(parts)


def xor(data: bytes, key: str) -> bytes:
    kb = key.encode("utf-8")
    return bytes(b ^ kb[i % len(kb)] for i, b in enumerate(data))


def junk_lines(n: int) -> str:
    """A handful of harmless decoy locals to pad/obscure the wrapper."""
    out = []
    for _ in range(n):
        name = "_" + "".join(random.choice("Il1O0") for _ in range(random.randint(6, 12)))
        kind = random.randint(0, 2)
        if kind == 0:
            out.append("local %s = function() return %d end" % (name, random.randint(0, 9999)))
        elif kind == 1:
            out.append("local %s = '%s'" % (name, "".join(
                random.choice("0123456789abcdef") for _ in range(random.randint(8, 24)))))
        else:
            out.append("local %s = %d" % (name, random.randint(1000, 999999)))
    return "\n".join(out)


def build_wrapper(b64_payload: str) -> str:
    # Randomised identifiers so the wrapper doesn't read like a known template.
    v_b64 = "_" + "".join(random.choice("Il1O0") for _ in range(8))
    v_key = "_" + "".join(random.choice("Il1O0") for _ in range(8))
    v_dec = "_" + "".join(random.choice("Il1O0") for _ in range(8))
    v_xor = "_" + "".join(random.choice("Il1O0") for _ in range(8))
    v_src = "_" + "".join(random.choice("Il1O0") for _ in range(8))
    v_fn = "_" + "".join(random.choice("Il1O0") for _ in range(8))

    return f"""-- Mirage  |  do not edit this file by hand; it is generated from source.
-- (Casual copy-blocker only — the real source lives in the repo's cinematic/.)
{junk_lines(6)}
local {v_dec} = function(d)
	local b = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/'
	d = (d:gsub('[^'..b..'=]', ''))
	return (d:gsub('.', function(x)
		if x == '=' then return '' end
		local r, f = '', (b:find(x, 1, true) - 1)
		for i = 6, 1, -1 do r = r .. (f % 2 ^ i - f % 2 ^ (i - 1) > 0 and '1' or '0') end
		return r
	end):gsub('%d%d%d?%d?%d?%d?%d?%d?', function(x)
		if #x ~= 8 then return '' end
		local c = 0
		for i = 1, 8 do c = c + (x:sub(i, i) == '1' and 2 ^ (8 - i) or 0) end
		return string.char(c)
	end))
end
{junk_lines(5)}
local {v_xor} = function(s, k)
	local t = {{}}
	for i = 1, #s do
		t[i] = string.char(bit32.bxor(string.byte(s, i), string.byte(k, (i - 1) % #k + 1)))
	end
	return table.concat(t)
end
{junk_lines(4)}
local {v_key} = "{KEY}"
local {v_b64} = "{b64_payload}"
local {v_src} = {v_xor}({v_dec}({v_b64}), {v_key})
local {v_fn} = loadstring or load
if {v_fn} then {v_fn} = {v_fn}({v_src}) end
if {v_fn} then {v_fn}() end
"""


def main() -> None:
    os.makedirs(OUT_DIR, exist_ok=True)
    payload = build_payload()
    b64 = base64.b64encode(xor(payload.encode("utf-8"), KEY)).decode("ascii")
    wrapper = build_wrapper(b64)
    with open(OUT, "w", encoding="utf-8") as f:
        f.write(wrapper)
    print("wrote %s  (%d KB, payload %d KB)" % (
        os.path.relpath(OUT, ROOT), len(wrapper) // 1024, len(payload) // 1024))


if __name__ == "__main__":
    main()
